public class LokalnyNode {

    private final double ksi;
    private final double eta;

    public LokalnyNode(double ksi, double eta) {
        this.ksi = ksi;
        this.eta = eta;
    }

    public double getKsi() {
        return ksi;
    }

    public double getEta() {
        return eta;
    }

}
